export async function listWorkspaces() {
  const workspaces = await readRegistry();
  if (workspaces.length === 0) {
    console.log('No initialized workspaces found.');
    return [];
  }

  workspaces.forEach((workspace, index) => {
    console.log(`${index + 1}) ${workspace}`);
  });
  return workspaces;
}

export async function chooseRegisteredWorkspace(workspaces, message = 'Search workspaces') {
  if (workspaces.length === 0) return null;

  return search({
    message,
    source: async (term) => {
      const query = (term ?? '').toLowerCase();
      const filtered = workspaces.filter((workspace) => {
        if (!query) return true;
        return workspace.toLowerCase().includes(query) || path.basename(workspace).toLowerCase().includes(query);
      });

      return filtered.slice(0, 20).map((workspace) => ({
        value: workspace,
        name: path.basename(workspace),
        description: workspace,
        short: workspace,
      }));
    },
  });
}

export async function promptForWorkspaceSelection() {
  const registered = await readRegistry();
  if (registered.length === 0) {
    console.log('No initialized workspaces found. Create one with `diagram-workspace new`.');
    return null;
  }

  return chooseRegisteredWorkspace(registered, 'Search workspaces');
}

export async function confirmWorkspaceDeletion(workspaceDir) {
  return confirm({
    message: `Delete workspace ${workspaceDir}? This removes the folder and unregisters it.`,
    default: false,
  });
}

export async function deleteWorkspace(workspaceDir) {
  const normalized = path.resolve(expandHome(workspaceDir));
  if (isProtectedWorkspace(normalized)) {
    throw new Error(`workspace is protected and cannot be deleted: ${normalized}`);
  }
  await fs.rm(normalized, { recursive: true, force: true });
  await removeWorkspaceFromRegistry(normalized);
  return normalized;
}

export async function promptForNewFolder(message, defaultValue) {
  const answer = await input({
    message,
    default: defaultValue,
    validate: (value) => {
      if (!value.trim()) return 'Please enter a folder path.';
      return true;
    },
  });

  return path.resolve(expandHome(answer.trim()));
}

async function listVisibleSubdirectories(directory) {
  const entries = await fs.readdir(directory, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => entry.name)
    .filter((name) => !name.startsWith('.'))
    .sort((a, b) => a.localeCompare(b));
}

export async function promptForWorkspaceFolder(startDir = path.join(os.homedir(), 'Projects', 'diagrams')) {
  let currentDir = path.resolve(expandHome(startDir));
  await fs.mkdir(currentDir, { recursive: true });

  while (true) {
    const subdirectories = await listVisibleSubdirectories(currentDir);
    const choices = [];

    if (currentDir !== path.parse(currentDir).root) {
      choices.push({
        value: 'parent',
        name: '..',
        description: path.dirname(currentDir),
      });
    }

    choices.push({
      value: 'use',
      name: 'Use this folder',
      description: currentDir,
    });

    choices.push({
      value: 'new',
      name: 'Create folder here',
      description: 'Make a new child folder and use it.',
    });

    for (const name of subdirectories) {
      const fullPath = path.join(currentDir, name);
      choices.push({
        value: fullPath,
        name: `📁 ${name}`,
        description: fullPath,
      });
    }

    choices.push({
      value: 'manual',
      name: 'Type a path manually',
      description: 'Enter a folder path directly.',
    });

    choices.push({
      value: 'cancel',
      name: 'Cancel',
      description: 'Abort workspace creation.',
    });

    const selection = await select({
      message: `Choose a workspace folder: ${currentDir}`,
      choices,
    });

    if (selection === 'use') {
      return currentDir;
    }

    if (selection === 'parent') {
      currentDir = path.dirname(currentDir);
      continue;
    }

    if (selection === 'new') {
      const childName = await input({
        message: 'New folder name',
        validate: (value) => {
          if (!value.trim()) return 'Please enter a folder name.';
          if (value.includes(path.sep)) return 'Please enter a single folder name, not a path.';
          return true;
        },
      });

      const targetDir = path.join(currentDir, childName.trim());
      await fs.mkdir(targetDir, { recursive: true });
      return targetDir;
    }

    if (selection === 'manual') {
      return promptForNewFolder('Workspace folder', currentDir);
    }

    if (selection === 'cancel') {
      return null;
    }

    currentDir = selection;
  }
}
